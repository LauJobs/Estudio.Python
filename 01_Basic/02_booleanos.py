#operadores de comparacion: devuelven un valor booleano
print("5 < 3:", 5 < 3)
print("5 > 3:", 5 > 3)
print("5 == 3:", 5 == 3)
print("5 != 3:", 5 != 3)
print("5 <= 3:", 5 <= 3)
print("5 >= 3:", 5 >= 3)

print("comparacion de cadenas:")
print("'hola' == 'Hola':", 'Hola' == 'hola')
print("'manzana'< 'pera': ", "manzana" < "pera")  # Comparación lexicográfica (numero alfabetico)

#operadores logicos
